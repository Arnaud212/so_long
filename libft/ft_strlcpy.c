#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t dstsize)
{
	size_t	i;
	size_t	x;

	i = 0;
	x = 0;
	if (dst == NULL || src == NULL)
		return (0);
	while (src[x] != '\0')
		x++;
	if (dstsize == 0)
		return (x);
	while ((src[i] != '\0') && (i < dstsize - 1))
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (x);
}
